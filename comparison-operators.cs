
int a = 10;
int b = 4;


// == equal
bool c = a == b; // false

// != not equal
bool c = a != b;    // true
bool d = a != 10;     // false

// < less than
bool c = a < b; // false

// > more than
bool c = a > b;     // true
bool d = a > 25;    // false

// <=
bool c = a <= b;     // false
bool d = a <= 25;    // true

// >=
bool c = a >= b;     // true
bool d = a >= 25;    // false